import numpy as np
import pandas as pd
from numpy.linalg import inv, det

# Исходные данные
data = {
    'y': [55, 80, 55, 83, 86, 107, 85, 50, 75, 50, 95, 40, 100, 40, 60],
    'x1': [14, 11, 14, 8, 8, 7, 7, 14, 7, 17, 7, 14, 6, 18, 12],
    'x2': [100, 150, 195, 118, 77, 100, 100, 180, 79, 90, 100, 184, 76, 83, 100]
}

df = pd.DataFrame(data)
n = len(df)

print("=" * 60)
print("ПРАКТИЧЕСКОЕ ЗАНЯТИЕ №2")
print("Множественная линейная регрессия для ВАЗ 2107")
print("=" * 60)

# 1. Расчет средних значений
y_mean = df['y'].mean()
x1_mean = df['x1'].mean()
x2_mean = df['x2'].mean()

print(f"\n1. СРЕДНИЕ ЗНАЧЕНИЯ:")
print(f"   y̅ = {y_mean:.4f} тыс. руб.")
print(f"   x̅₁ = {x1_mean:.4f} лет")
print(f"   x̅₂ = {x2_mean:.4f} тыс. км")

# 2. Расчет средних квадратических отклонений
y_std = df['y'].std(ddof=1)
x1_std = df['x1'].std(ddof=1)
x2_std = df['x2'].std(ddof=1)

print(f"\n2. СРЕДНИЕ КВАДРАТИЧЕСКИЕ ОТКЛОНЕНИЯ:")
print(f"   σy = {y_std:.4f}")
print(f"   σx₁ = {x1_std:.4f}")
print(f"   σx₂ = {x2_std:.4f}")

# 3. Расчет коэффициентов парной корреляции Пирсона
r_yx1 = df['y'].corr(df['x1'])
r_yx2 = df['y'].corr(df['x2'])
r_x1x2 = df['x1'].corr(df['x2'])

print(f"\n3. КОЭФФИЦИЕНТЫ ПАРНОЙ КОРРЕЛЯЦИИ:")
print(f"   r(y,x₁) = {r_yx1:.4f}")
print(f"   r(y,x₂) = {r_yx2:.4f}")
print(f"   r(x₁,x₂) = {r_x1x2:.4f}")

# 4. Построение уравнения множественной регрессии
# Метод наименьших квадратов
X = np.column_stack([np.ones(n), df['x1'], df['x2']])
y = df['y'].values

# Расчет коэффициентов: b = (X'X)^(-1) X'y
X_transpose = X.T
X_transpose_X = X_transpose @ X
X_transpose_y = X_transpose @ y

# Проверка определителя
determinant = det(X_transpose_X)
print(f"\n4. ОПРЕДЕЛИТЕЛЬ МАТРИЦЫ (X'X): {determinant:.4f}")

if abs(determinant) < 1e-10:
    print("   ВНИМАНИЕ: Определитель близок к нулю!")
else:
    print("   Определитель далек от нуля - решение существует.")

# Нахождение коэффициентов
b = inv(X_transpose_X) @ X_transpose_y
b0, b1, b2 = b

print(f"\n5. КОЭФФИЦИЕНТЫ РЕГРЕССИИ:")
print(f"   b₀ = {b0:.4f}")
print(f"   b₁ = {b1:.4f}")
print(f"   b₂ = {b2:.4f}")

print(f"\n6. УРАВНЕНИЕ МНОЖЕСТВЕННОЙ РЕГРЕССИИ:")
print(f"   y = {b0:.4f} {b1:+.4f}·x₁ {b2:+.4f}·x₂")

print(f"\n   ИНТЕРПРЕТАЦИЯ:")
print(f"   • При увеличении возраста автомобиля на 1 год")
print(f"     цена изменяется в среднем на {b1:.4f} тыс. руб.")
print(f"   • При увеличении пробега на 1 тыс. км")
print(f"     цена изменяется в среднем на {b2:.4f} тыс. руб.")
print(f"   • Средняя цена нового автомобиля (возраст=0, пробег=0):")
print(f"     {b0:.4f} тыс. руб.")

# 5. Расчет коэффициентов частной корреляции
# r(y,x₁|x₂) - частная корреляция между y и x₁ при исключении влияния x₂
# r(y,x₂|x₁) - частная корреляция между y и x₂ при исключении влияния x₁

# Знаменатель для обоих коэффициентов
denom_1 = np.sqrt((1 - r_yx2**2) * (1 - r_x1x2**2))
denom_2 = np.sqrt((1 - r_yx1**2) * (1 - r_x1x2**2))

r_yx1_x2 = (r_yx1 - r_yx2 * r_x1x2) / denom_1
r_yx2_x1 = (r_yx2 - r_yx1 * r_x1x2) / denom_2

print(f"\n7. КОЭФФИЦИЕНТЫ ЧАСТНОЙ КОРРЕЛЯЦИИ:")
print(f"   r(y,x₁|x₂) = {r_yx1_x2:.4f}")
print(f"   r(y,x₂|x₁) = {r_yx2_x1:.4f}")

# Интерпретация по шкале Чеддока
def cheddock_scale(r):
    abs_r = abs(r)
    if abs_r < 0.3:
        return "очень слабая"
    elif abs_r < 0.5:
        return "слабая"
    elif abs_r < 0.7:
        return "заметная"
    elif abs_r < 0.9:
        return "тесная"
    else:
        return "весьма тесная"

print(f"\n   ИНТЕРПРЕТАЦИЯ (шкала Чеддока):")
print(f"   • Связь цены с возрастом (без учета пробега):")
print(f"     {cheddock_scale(r_yx1_x2)} ({'прямая' if r_yx1_x2 > 0 else 'обратная'})")
print(f"   • Связь цены с пробегом (без учета возраста):")
print(f"     {cheddock_scale(r_yx2_x1)} ({'прямая' if r_yx2_x1 > 0 else 'обратная'})")

# 6. Расчет коэффициента множественной корреляции
# R² = (r_yx1² + r_yx2² - 2·r_yx1·r_yx2·r_x1x2) / (1 - r_x1x2²)

R_squared = (r_yx1**2 + r_yx2**2 - 2*r_yx1*r_yx2*r_x1x2) / (1 - r_x1x2**2)
R = np.sqrt(R_squared)

print(f"\n8. КОЭФФИЦИЕНТ МНОЖЕСТВЕННОЙ КОРРЕЛЯЦИИ:")
print(f"   R = {R:.4f}")

print(f"\n9. КОЭФФИЦИЕНТ МНОЖЕСТВЕННОЙ ДЕТЕРМИНАЦИИ:")
print(f"   R² = {R_squared:.4f}")
print(f"   R² = {R_squared*100:.2f}%")

print(f"\n10. ВЫВОДЫ:")
print(f"   • {R_squared*100:.2f}% вариации цены автомобиля объясняется")
print(f"     вариацией возраста и пробега")
print(f"   • {(1-R_squared)*100:.2f}% приходится на неучтенные факторы")

if R_squared > 0.7:
    print(f"   • Модель имеет ХОРОШУЮ объясняющую способность")
elif R_squared > 0.5:
    print(f"   • Модель имеет УДОВЛЕТВОРИТЕЛЬНУЮ объясняющую способность")
else:
    print(f"   • Модель имеет НИЗКУЮ объясняющую способность")

# Рекомендации
print(f"\n11. РЕКОМЕНДАЦИИ:")
if abs(r_yx2_x1) < 0.3:
    print(f"   • Пробег очень слабо коррелирует с ценой")
    print(f"   • Рекомендуется исключить фактор 'пробег' из модели")
    print(f"   • Добавить новые факторы (состояние, комплектация и др.)")

print("\n" + "=" * 60)
print("РАБОТА ВЫПОЛНЕНА")
print("=" * 60)